#Viết chương trình Python cho người dùng nhập vào một số nguyên. 
#Thực hiện việc đổi số đó sang kiểu string, float, boolean
#In kết quả từng kiểu ra màn hình
a = int(input("Nhập số nguyên:"))
soThuc = float(a)
Chuoi = str(a)
Boolean = bool(a)
print("Số thực đã chuyển là:",soThuc)
print("Chuỗi đã chuyển là:",Chuoi)
print("Dữ liệu bool đã chuyển là:",Boolean)
